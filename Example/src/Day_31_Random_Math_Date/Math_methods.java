package Day_31_Random_Math_Date;

public class Math_methods {
    public static void main(String[] args) {

        System.out.println(Math.PI); // PI
        System.out.println(Math.E); // экспонента
        System.out.println(Math.sqrt(25)); // корень квадратный
        System.out.println(Math.cbrt(8)); // корень кубический
        System.out.println(Math.pow(2,4)); // возведение в степень
        System.out.println(Math.sin(30)); // синус угла
        System.out.println(Math.round(5.4)); // округление числа по математическим принципам
        System.out.println(Math.ceil(5.1)); // округление числа вверх
        System.out.println(Math.floor(5.1)); // округление числа вниз
        System.out.println(Math.max(Math.max(5,1),6)); // выбор максимального числа
        System.out.println(Math.random()*100); //возвращает псевдослучайное число до 99,
        System.out.println(Math.random()); //возвращает псевдослучайное число с запятой от 0 до 1
    }
}
